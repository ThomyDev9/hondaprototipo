    estado_civil VARCHAR(128) DEFAULT NULL,
    actividad_economica VARCHAR(255) DEFAULT NULL,
    telefono_domicilio VARCHAR(64) DEFAULT NULL,
    telefono_celular VARCHAR(64) DEFAULT NULL,
    monto_solicitado VARCHAR(64) DEFAULT NULL,
    estado VARCHAR(255) DEFAULT NULL,
    sub_estado VARCHAR(255) DEFAULT NULL,
    observacion TEXT,
    proceso_a_realizar TEXT,
    monto_aplica VARCHAR(64) DEFAULT NULL,
    observacion_cooperativa TEXT,
    fecha_contacto VARCHAR(64) DEFAULT NULL,
    estatus VARCHAR(255) DEFAULT NULL,
